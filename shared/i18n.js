  function isSupportedLanguage(language) {
    return Object.prototype.hasOwnProperty.call(MESSAGES, language);
  }

  function fillTemplate(message, params = {}) {
    return String(message || "").replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ""));
  }

  function translateUi(language, key, params = {}) {
    const selected = isSupportedLanguage(language) ? language : fallbackLanguage;
    const table = MESSAGES[selected] || MESSAGES[fallbackLanguage] || {};
    const fallbackTable = MESSAGES.en || {};
    const message = table[key] || (MESSAGES.ko && MESSAGES.ko[key]) || fallbackTable[key] || key;
    return fillTemplate(message, params);
  }

